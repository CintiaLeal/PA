 private void listarplataforma() {
        List<DTPlataforma> list = sis.listarPlataformas();
        DefaultListModel modelo = new DefaultListModel();
        for (int i = 0; i < list.size(); i++) {
            String pnombre = list.get(i).getNombre();
            modelo.addElement(pnombre);
        }
        jList3.setModel(modelo);
    }

 
 
 private void listarpaquetes() {
        List<DTPaquetes> list = sis.listarPaquete();
        DefaultListModel model = new DefaultListModel();
        for (int i = 0; i < list.size(); i++) {
            String nombre = list.get(i).getNombre();
            model.addElement(nombre);
        }
        jList2.setModel(model);
}


private void listarEspectaculosNoSonParte(){
    List<String> list = sis.listarEspectaculoQueNoFormanParte(jPaquetes.getText(),jPlataforma.getText());
    DefaultListModel model = new DefaultListModel();
    for (int i = 0; i < list.size(); i++) {
            model.addElement(list.get(i));
    }
    
    jList4.setModel(model);

}

