private void listarPlataforma() {
        List<String> res = sis.listarPlataforma();
        DefaultListModel modelo = new DefaultListModel();
        for(int i = 0; i < res.size(); i++){
            modelo.addElement(res.get(i));
        }
        jList2.setModel(modelo);
    }

 private void listarPlataformaE() {
        List<String> res = sis.listarEspectaculoPl(jTextField1.getText());
        DefaultListModel modelo = new DefaultListModel();
        for(int i = 0; i < res.size(); i++){
            modelo.addElement(res.get(i));
        }
        jList3.setModel(modelo);
    }
 
 private void listarCategorias(){
     List<String> res = sis.listarCategoriaE(jTextField2.getText());
     for(int i = 0; i < res.size(); i++){
            String pnombre = res.get(i);
            jComboBox1.addItem(pnombre);
        }
 
 }
 
  private void listarPaquetes(){
     List<String> res = sis.listarPaqueteEspectaculo(jTextField2.getText());
     for(int i = 0; i < res.size(); i++){
            String pnombre = res.get(i);
            paquetes.addItem(pnombre);
        }
 
 }

