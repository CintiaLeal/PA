    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        if ((nombre.getText().length() != 0) && (descripcion.getText().length() != 0) && (url.getText().length() != 0)) {
            String nom = nombre.getText();
            String des = descripcion.getText();
            String url_ = url.getText();

            if (sis.existePlataforma(nom)) {

                JOptionPane.showMessageDialog(null, "YA EXISTE UNA PLATAFORMA CON ESE NOMBRE", "Error...", JOptionPane.ERROR_MESSAGE);
                nombre.setText("");

            } else {

                sis.altaDePlataforma(nom, url_, des);
                JOptionPane.showMessageDialog(null, "Creada correctamente!!", "La plataforma fue creada exitosamente", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }

        } else {
            JOptionPane.showMessageDialog(null, "No pueden quedar campos sin completar", "Error...", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_aceptarActionPerformed
    }
       
       
