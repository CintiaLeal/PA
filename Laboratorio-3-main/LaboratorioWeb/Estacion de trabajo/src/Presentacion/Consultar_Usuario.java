    private void listarUsuarios() {
        List<String> res = sis.listarUsuarios();
        DefaultListModel modelo = new DefaultListModel();
        for(int i = 0; i < res.size(); i++){
            modelo.addElement(res.get(i));
        }
        jList2.setModel(modelo);
    }
